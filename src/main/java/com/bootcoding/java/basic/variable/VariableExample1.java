package com.bootcoding.java.basic.variable;

public class VariableExample1 {
    public static void main(String args[]) {
        int a = 100;
        int b = 20;
        int sum = a + b;
        int mul = a * b;
        int sub = a - b;
        int div = a / b;
        System.out.println("Addition of " + a + " and " + b + " = " + sum);
        // Multiplication of 100 and 20 = 2000
        // Subtraction of 100 and 20 = 80
        // Division of 100 and 20 = 5
        byte aa = 100;
        byte bb = 20;
        int summ = aa + bb;
        int mull = aa * bb;
        int subb = aa - bb;
        int divv = aa / bb;

        System.out.println("\n\nByte Example =====");
        //byte, short and long

    }
}
